class Vehicle {
  final String title;
  final String subtitle;
  final String image;
  final String price;
  final String fuelCapacity;
  final String weight;
  final String seats;

  Vehicle({
    required this.title,
    required this.subtitle,
    required this.image,
    required this.price,
    required this.fuelCapacity,
    required this.weight,
    required this.seats,
  });
}
